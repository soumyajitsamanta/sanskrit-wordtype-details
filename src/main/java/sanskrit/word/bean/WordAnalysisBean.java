package sanskrit.word.bean;

public class WordAnalysisBean {
    private String vibhakti;
    private String lakar;
    private String mul;
    private String shabd;
    

    public String getVibhakti() {
        return vibhakti;
    }

    public void setVibhakti(String vibhakti) {
        this.vibhakti = vibhakti;
    }

    public String getLakar() {
        return lakar;
    }

    public void setLakar(String lakar) {
        this.lakar = lakar;
    }

    public String getMul() {
        return mul;
    }

    public void setMul(String mul) {
        this.mul = mul;
    }

    public String getShabd() {
        return shabd;
    }

    public void setShabd(String shabd) {
        this.shabd = shabd;
    }

}
